                        <p style="width: 100%; overflow-wrap: normal;">
                            Annelie Smits (1996), geboren en getogen in De Goorn, wist al op jonge leeftijd dat ze iets met muziek wilde doen. 
                            Zingen is haar grootste passie en soms begeleidt ze zichzelf  hiermee achter de piano. Haar muzikale carrière 
                            begon al vroeg: toen ze zeven jaar was, leerde ze voor het eerst noten kennen op blokfluitles. 
                            Ook ging zij op cornetles, wat ze totaal acht jaar heeft gedaan bij Brassband Kunst naar Kracht in De Goorn. 
                            Op 15-jarige leeftijd besloot ze haar horizon te verbreden en heeft ze zich opgegeven voor zowel zangles 
                            als pianoles bij Muziekschool Koggenland in De Goorn. 
                            Ondanks dat ze slechts één keer in de twee weken zangles had, groeide ze hard. 
                            Onder begeleiding van zangdocente Anita Rol werden haar vocale eigenschappen steeds beter en ontwikkelde ze een 
                            heldere, zuivere en warme stem. Na vier jaar was ze toe aan een nieuwe uitdaging: ze nam in september 2015 
                            zangles bij zangschool Pop-up in Hoorn, zodat ze andere zangtechnieken kon leren. 
                        </p>

                        <p style="width: 100%; overflow-wrap: normal;">
                            In de afgelopen jaren zijn veel van haar dromen uitgekomen. 
                            Van kerstconcert tot amusementstocht: ze trad overal op. Daarnaast is Annelie sinds september 2013 de 
                            zangeres van de <a target="_blank" href="https://www.facebook.com/thebandjamm/?fref=ts" >coverband Jamm</a>, waarmee ze regelmatig optreedt op zowel besloten feesten als kermissen. 
                            Ook heeft ze een eigen videoclip opgenomen in <a href="http://www.studio747.nl/">Studio 747</a> van Maarten Schelvis, waarin ze 
                            <a target="_blank" href="https://www.youtube.com/watch?v=x4lYldgNHPU">‘Mag ik dan bij jou’</a> van Claudia de Breij zingt. Bovendien heeft ze diverse jingles voor lokale radiostations ingezongen. 
                            Daarnaast schrijft Annelie zelf nummers, waarin ze al haar creativiteit kwijt kan. Tevens wilde ze professioneler te 
                            werk, wat ze realiseerde door opnameapparatuur en in-ears aan te schaffen. 
                            Kortom, aan motivatie geen gebrek. Haar levensmotto is dan ook niets voor niets ‘don’t dream your life, but live your dreams!’.
                        </p>

